const getSemesterNumber = (educationTypeData, semesterNum) => {
  let count = 0;
  for (const year of educationTypeData.years) {
    for (const sem of year.semesters) {
      count += 1;
      if (count === semesterNum) return { yearNumber: year.yearNumber, semesterNumber: sem.semesterNumber };
    }
  }
  return null;
};

const dropStaleIndexes = async () => {
  try {
    const sharedSubjectCol = SharedSubject.collection;
    const groupCol = Group.collection;
    const indexesToDrop = ['name_1_course_1'];
    for (const idx of indexesToDrop) {
      try { await sharedSubjectCol.dropIndex(idx); } catch {}
      try { await groupCol.dropIndex(idx); } catch {}
    }
  } catch {}
};

let _seedVerified = false;

const ensureAcademicDefaults = async () => {
  if (_seedVerified) {
    return { educations: 0, groups: 0, subjects: 0, semesters: 0, cached: true };
  }

  try {
    await dropStaleIndexes();
    const result = { educations: 0, groups: 0, subjects: 0, semesters: 0 };

    for (const eduData of seedData) {
      const existingEdu = await Education.findOne({ name: eduData.educationType });
      if (!existingEdu) {
        await Education.create({ name: eduData.educationType, description: eduData.description });
        result.educations += 1;
      }

      for (const year of eduData.years) {
        for (const sem of year.semesters) {
          const existingSem = await Semester.findOne({
            educationType: eduData.educationType,
            semesterNumber: sem.semesterNumber,
          });
          if (!existingSem) {
            await Semester.create({
              educationType: eduData.educationType,
              yearNumber: year.yearNumber,
              semesterNumber: sem.semesterNumber,
              label: `${year.label} - ${sem.label}`,
            });
            result.semesters += 1;
          }
        }
      }
    }

    const activeGroupKeys = new Set();

    for (const eduData of seedData) {
      for (const groupData of eduData.groups) {
        activeGroupKeys.add(`${eduData.educationType}:${groupData.name}`);
        let groupDoc = await Group.findOne({ name: groupData.name, educationType: eduData.educationType });
        if (!groupDoc) {
          groupDoc = await Group.create({
            name: groupData.name,
            educationType: eduData.educationType,
            description: `${groupData.name} - ${eduData.educationType}`,
          });
          result.groups += 1;
        }

        for (const subjectData of groupData.subjects) {
          const semesterDoc = await Semester.findOne({
            educationType: eduData.educationType,
            semesterNumber: subjectData.semester,
          });

          const existing = await SharedSubject.findOne({ name: subjectData.name, group: groupDoc._id });
          if (!existing) {
            await SharedSubject.create({
              name: subjectData.name,
              group: groupDoc._id,
              semester: semesterDoc?._id,
              description: `${subjectData.name} for ${groupData.name} - ${eduData.educationType}`,
            });
            result.subjects += 1;
          } else if (semesterDoc && !existing.semester) {
            existing.semester = semesterDoc._id;
            await existing.save();
          }
        }
      }
    }

    const seededEducationTypes = seedData.map((item) => item.educationType);
    const seededGroups = await Group.find({ educationType: { $in: seededEducationTypes } });
    for (const group of seededGroups) {
      const shouldBeActive = activeGroupKeys.has(`${group.educationType}:${group.name}`);
      if (group.isActive !== shouldBeActive) {
        group.isActive = shouldBeActive;
        await group.save();
      }
    }

    _seedVerified = true;
    return result;
  } catch (error) {
    console.error('Error in ensureAcademicDefaults:', error.stack || error);
    throw error;
  }
};

export const seedAcademicData = async (req, res) => {
  try {
    const created = await ensureAcademicDefaults();

    const io = getIO();
    if (io) io.emit('academic:updated', { type: 'seed', action: 'upsert' });

    const statusCode = created.groups || created.subjects ? 201 : 200;
    res.status(statusCode).json({
      success: true,
      message: `Academic data ready. Added ${created.groups} groups, ${created.subjects} subjects, ${created.semesters} semesters.`,
      data: created,
    });
  } catch (error) {
    res.status(500).json({ success: false, message: error.message });
  }
};

